var rateNum = document.getElementsByClassName("rate-num")[0];
var reviewsNum = document.getElementsByClassName("reviews-num")[0];
var mealImg = document.getElementsByClassName("meal-img")[0];

function chooseRandomMeal() {
  var index;
  do {
    index = Math.floor(Math.random() * meals.length);
  } while (index === previousIndex);

  previousIndex = index;

  mealName.innerHTML = meals[index].mealName;
  mealDesc.innerHTML = meals[index].desc;
  ingredientsList.innerHTML = "";
  for (var i = 0; i < meals[index].ingredients.length; i++) {
    ingredientsList.innerHTML += `<li>${meals[index].ingredients[i]}</li>`;
  }
  instructionsList.innerHTML = "";
  for (var i = 0; i < meals[index].instructions.length; i++) {
    instructionsList.innerHTML += `<li>${meals[index].instructions[i]}</li>`;
  }

  calories.innerHTML = meals[index].calories;
  protein.innerHTML = meals[index].protein;
  carbohydrates.innerHTML = meals[index].carbohydrates;
  fat.innerHTML = meals[index].fat;
  fiber.innerHTML = meals[index].fiber;
  sodium.innerHTML = meals[index].sodium;

  chefTipsList.innerHTML = "";
  for (var i = 0; i < meals[index].chefTips.length; i++) {
    chefTipsList.innerHTML += `<li class="rounded-3"><i class="fa-solid fa-circle-check"></i> ${meals[index].instructions[i]}</li>`;
  }

  mealCountry.innerHTML = meals[index].country;
  mealDifficulty.innerHTML = meals[index].difficulty;
  prepTime.innerHTML = meals[index].prepTime;
  cookTime.innerHTML = `${meals[index].cookTime} min`;
  servings.innerHTML = meals[index].Servings;

  if (meals[index].cookTime > 45) {
    timeCookedWarning.style.display = "flex";
  } else {
    timeCookedWarning.style.display = "none";
  }

  rateNum.innerHTML = meals[index].rate;
  reviewsNum.innerHTML = meals[index].reviews;

  mealImg.src = meals[index].mealImg;
}

chooseRandomMeal();
